document.addEventListener('DOMContentLoaded', () => {
  const ruleHttps = document.getElementById('rule-https');
  const ruleWww = document.getElementById('rule-www');
  const ruleIndexes = document.getElementById('rule-indexes');
  const ruleHotlink = document.getElementById('rule-hotlink');
  const output = document.getElementById('htaccess-output');
  const btnCopy = document.getElementById('btn-copy-htaccess');

  const checkboxes = [ruleHttps, ruleWww, ruleIndexes];
  checkboxes.forEach(c => c.addEventListener('change', generateHtaccess));
  ruleHotlink.addEventListener('input', generateHtaccess);

  btnCopy.addEventListener('click', () => {
    navigator.clipboard.writeText(output.value);
    alert('Copied .htaccess rules to clipboard!');
  });

  function generateHtaccess() {
    let code = "# ==========================================\n# GENERATED BY LITECONVERT\n# ==========================================\n\nRewriteEngine On\n\n";

    if (ruleHttps.checked) {
      code += "# Redirect HTTP to HTTPS\nRewriteCond %{HTTPS} off\nRewriteRule ^(.*)$ https://%{HTTP_HOST}%{REQUEST_URI} [L,R=301]\n\n";
    }

    if (ruleWww.checked) {
      code += "# Redirect non-WWW to WWW\nRewriteCond %{HTTP_HOST} !^www\\. [NC]\nRewriteRule ^(.*)$ http://www.%{HTTP_HOST}%{REQUEST_URI} [L,R=301]\n\n";
    }

    if (ruleIndexes.checked) {
      code += "# Prevent Directory Listings\nOptions -Indexes\n\n";
    }

    const domain = ruleHotlink.value.trim();
    if (domain) {
      const escapedDomain = domain.replace(/\./g, '\\.');
      code += `# Prevent Image Hotlinking\nRewriteCond %{HTTP_REFERER} !^$\nRewriteCond %{HTTP_REFERER} !^http(s)?://(www\\.)?${escapedDomain} [NC]\nRewriteRule \\.(jpg|jpeg|png|gif|svg)$ - [F,NC,L]\n\n`;
    }

    output.value = code.trim();
    debouncedStatIncrement();
  }

  generateHtaccess();

  let statsTimeout = null;
  function debouncedStatIncrement() {
    if (statsTimeout) clearTimeout(statsTimeout);
    statsTimeout = setTimeout(async () => {
      try {
        await fetch('/api/stats/increment', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ tool_key: 'htaccess-generator' })
        });
      } catch (err) {
        console.error('Failed to log stats:', err);
      }
    }, 2000);
  }
});
